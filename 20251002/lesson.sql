-- Exec 2
SELECT vendedor, produto, valor 
FROM VENDAS 
WHERE valor = (SELECT MAX(valor) FROM VENDAS);

-- Exec 3
SELECT categoria, SUM(valor) as vendido 
FROM VENDAS 
WHERE valor > (SELECT AVG(valor) as media 
FROM VENDAS)
GROUP BY categoria;

-- Exec 4
